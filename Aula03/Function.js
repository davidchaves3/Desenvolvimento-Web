/*
  As funções de seta (arrow functions) são uma forma mais concisa de escrever funções em JavaScript.
  Elas são especialmente úteis para funções curtas e podem tornar o código mais legível.
*/
// Arrow Function
const soma = (a, b) => a + b;

console.log(soma(5, 10)); 

// Função Tradicional
function subtrai(a, b) {
  return a - b;
}

console.log(subtrai(10, 5));

